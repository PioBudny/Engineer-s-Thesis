//Wypisuje wszystkie rejestry
void dump_all_regs(i2c_inst_t *i2c)
{
    uint8_t data;

    printf("\n==============================\n");
    printf("8T49N241 regs:\n");
    printf("==============================\n");


    // ========================================
    // REJESTRY KONFIGURACYJNE
    // 0x0000 - 0x007B
    // ========================================

    for(uint16_t reg = 0x0000;
        reg <= 0x007B;
        reg++)
    {
        if(i2c_read_reg16(
            i2c,
            DEVICE_ADDR,
            reg,
            &data,
            1
        ))
        {
            printf(
                "REG[0x%04X] = 0x%02X\n",
                reg,
                data
            );
        }
        else
        {
            printf(
                "REG[0x%04X] = READ ERROR\n",
                reg
            );
        }

        sleep_us(100);
    }


    // ========================================
    // REJESTRY STATUSOWE
    // 0x0200 - 0x0212
    // ========================================

    printf("\nSTATUS REGS\n\n");

    for(uint16_t reg = 0x0200;
        reg <= 0x0212;
        reg++)
    {
        if(i2c_read_reg16(
            i2c,
            DEVICE_ADDR,
            reg,
            &data,
            1
        ))
        {
            printf(
                "REG[0x%04X] = 0x%02X\n",
                reg,
                data
            );
        }
        else
        {
            printf(
                "REG[0x%04X] = READ ERROR\n",
                reg
            );
        }

        sleep_us(100);
    }
}


bool load_tab(i2c_inst_t *i2c, const pll_cfg_t *cfg, size_t count)
{
    printf("Loading config...\n");

    for (size_t i = 0; i < count; i++)
    {
        uint16_t reg = cfg[i].reg;
        uint8_t value = cfg[i].value;

        // Próba zapisu rejestru
        if (!i2c_write_reg16(i2c, DEVICE_ADDR, reg, &value, 1))
        {
            sleep_ms(2); 
            if (!i2c_write_reg16(i2c, DEVICE_ADDR, reg, &value, 1)) {
                printf("Skip reg 0x%04X (NACK)\n", reg);
                continue;
            }
        }


    }

    printf("Config loaded successfully\n");
    sleep_ms(10);
    return true;
}